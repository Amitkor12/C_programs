/* What is a reserved word? Give examples. 
*/
/* Keywords are reserved, that is, you cannot use them as your identifiers. 
   As such, they are usually called reserved words.
*/

#include<stdio.h>
#include<string.h>

void main()
{
    int a = 45;
    char ch[10] = "Amit";
    float fl = 2.34;

    printf("int \tis a reserved keyword\n");
    printf("char \tis a reserved keyword\n");
    printf("float \tis a reserved keyword\n");
    
}